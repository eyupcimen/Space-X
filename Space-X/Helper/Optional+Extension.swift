import Foundation

public extension Optional {
    
    public struct FoundNilWhileUnwrappingError: Error { }
    
    public func unwrap() throws -> Wrapped {
        switch self {
        case .some(let wrapped):
            return wrapped
        case .none:
            throw FoundNilWhileUnwrappingError()
        }
    }
}
